package chapter2;

public class BasicMatch {
    public static void main(String[] args) {
        int a = 1 + 1;
        int b = a + 3;
        int c = b / 4;
        int d = c - a;
        int e = -a;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
